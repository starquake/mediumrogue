const svgDir = new URL("./svg/", import.meta.url);
const outPath = fileURLToPath(new URL("../../src/render/glyphIcons.ts", import.meta.url));

// inner markup of a game-icons svg, minus the 512×512 background rect, with fills
// stripped (the caller applies one fill for the whole glyph at draw time).
function inner(file) {
  const svg = readFileSync(new URL(file, svgDir), "utf8");
  const m = svg.match(/<svg[^>]*>([\s\S]*)<\/svg>/);
  let body = m ? m[1] : svg;
  body = body.replace(/<path[^>]*d="M0 0h512v512H0z"[^>]*\/>/g, ""); // bg rect
  body = body.replace(/\s*fill="[^"]*"/g, ""); // strip fills
  return body.trim();
}

const iconName = (file) => file.replace(/\.svg$/, "");
// Keys are monster-kind ids and class names, which may contain hyphens
// (kin-archer) — quote anything that is not a bare JS identifier, or the
// generated file is a TypeScript syntax error.
const tsKey = (k) => (/^[A-Za-z_$][A-Za-z0-9_$]*$/.test(k) ? k : `"${k}"`);
const entries = Object.entries(ICONS)
  .map(([k, v]) => `  ${tsKey(k)}: '${inner(v.file).replace(/'/g, "\\'")}',`)
  .join("\n");
const credits = Object.values(ICONS)
  .map((v) => `//   ${iconName(v.file)} — ${v.author}`)
  .join("\n");

const ts = `// Code generated by client/tools/glyph-icons/gen-glyph-icons.mjs. DO NOT EDIT.
// Regenerate with \`make icons\` after swapping a source SVG in
// client/tools/glyph-icons/svg.
//
// Monster-kind and player-class glyph icons, drawn dark on the entity's colored
// dot (see render/entities.ts). Vendored from game-icons.net as inline SVG path
// markup (512×512 viewBox) so no asset-loading pipeline is needed — Pixi v8's
// Graphics.svg() renders them directly. Background rects removed and fills
// stripped; a single fill is applied at draw time.
//
// Icons from https://game-icons.net — licensed CC BY 3.0
// (https://creativecommons.org/licenses/by/3.0/). Authors:
${credits}

/** Inner SVG markup (fill-stripped, 512×512) per glyph key. */
export const GLYPH_ICON_SVG: Record<string, string> = {
${entries}
};

/** The viewBox side length every glyph path is expressed in. */
export const GLYPH_ICON_VIEWBOX = 512;
`;

